package net.lepko.minecraft.easycrafting;

/**
 * RecipeType
 * 
 * Enum for the different easycraft recipe categories.
 * Used for determining what recipes are craftable by an easycraft block.
 * 
 * @author uberwaffe
 * 
 */
public enum RecipeType {
	ANY, TWOBYTWO, THREEBYTHREE, WOOD, METAL, ORE, TOOL, ARMOUR, DECORATION, MATERIAL, VANILLA, STONE, MISC1, MISC2, MISC3, MISC4, MISC5
}

